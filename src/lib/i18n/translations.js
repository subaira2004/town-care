export function t(key, lang = "en") {
  if (lang === "both" || lang === "bilingual") {
    const en = translations.en[key] || key;
    const ta = translations.ta[key] || key;
    if (en === ta) return en;
    return `${en} / ${ta}`;
  }
  return translations[lang]?.[key] || translations.en[key] || key;
}

export function getShareMessage(lang, data) {
  const template = t("shareTemplate", lang);
  return template
    .replace("{pharmacyName}", data.pharmacyName || "")
    .replace("{doctorName}", data.doctorName || "")
    .replace("{tokenNumber}", data.tokenNumber || "")
    .replace("{status}", data.status || "")
    .replace("{statusLink}", data.statusLink || "");
}

export function getStatusLabel(status, lang = "en") {
  const map = {
    waiting: "waiting",
    in_consultation: "inConsultation",
    completed: "completed",
    skipped: "skipped",
    cancelled: "cancelled",
    unattended: "unattended",
  };
  return t(map[status] || status, lang);
}

